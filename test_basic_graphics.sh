osc "screen;9"
osc "cls;0"
osc "line;0;0;640;60;1;BF"
osc "circle;320;40;20;14"
osc "paint;320;40;13;14"
osc "line;295;40;345;40;14"
osc "line;320;18;320;62;14"
osc "line;0;280;640;350;8;BF"
